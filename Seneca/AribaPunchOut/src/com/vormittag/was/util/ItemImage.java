package com.vormittag.was.util;

public class ItemImage
{
	private String thumbFileName = "";
	private String thumbAltText = "";
	private String fullFileName = "";
	private String fullAltText = "";
	
	public String getThumbFileName()
	{
		return thumbFileName;
	}
	public void setThumbFileName(String thumbFileName)
	{
		this.thumbFileName = thumbFileName;
	}
	public String getThumbAltText()
	{
		return thumbAltText;
	}
	public void setThumbAltText(String thumbAltText)
	{
		this.thumbAltText = thumbAltText;
	}
	public String getFullFileName()
	{
		return fullFileName;
	}
	public void setFullFileName(String fullFileName)
	{
		this.fullFileName = fullFileName;
	}
	public String getFullAltText()
	{
		return fullAltText;
	}
	public void setFullAltText(String fullAltText)
	{
		this.fullAltText = fullAltText;
	}
}